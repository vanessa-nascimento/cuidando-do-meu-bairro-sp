                Conheça alguns os termos do orçamento público mais importantes
            </h1>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Empenhado</h5>
            <p class="font-normal text-gray-700 ">Depois do orçamento, no qual o governo planeja seus gastos, inicia-se o processo de contratação de empresas, ou compra de materiais, e quando acontece esta contratação o governo faz a reserva destes recursos para o pagamento destas compras. A despesa é chamada de <b>Empenhada</b>, quando nasce a obrigação de pagamento da despesa pelo governo. Este, realiza a reserva daquele valor para que seja pago assim que o serviço prestado seja concluído e/ou o produto seja entregue para o poder público.</p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Contabilidade Pública</h5>
            <p class="font-normal text-gray-700 ">Controlar, gerenciar e monitorar os recursos públicos é uma tarefa complexa. O governo, em todas suas esferas, federal, estadual e municipal, tem a obrigação de desempenhar diversos papeis de suma importância para a sociedade, como a construção de postos de saúde, a compra de equipamentos, a contratação de funcionários, a manutenção de escolas e espaços públicos, entre diversas outras atividades.</p>
            
            <p class="font-normal text-gray-700 ">
                A contabilidade pública surge neste contexto. Ela tem o intuito de
                registrar e controlar os recursos públicos, a execução orçamentária, o
                patrimônio público e suas variações. Ou seja, a contabilidade pública
                é um instrumento de controle e acompanhamento da situação
                patrimonial das entidades governamentais. É por meio da
                contabilidade pública que podemos acompanhar como estão sendo
                aplicados os recursos da sociedade.</p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Receita Pública, Receita Primária, Receita Financeira</h5>
            <p class="font-normal text-gray-700 ">Os recursos do governo são fruto dos pagamentos dos impostos, taxas
                e contribuições, feitos pela população em geral. Em contabilidade
                chamamos estes recursos de Receita Pública. A receita pública é a
                origem dos recursos do governo, para que ele consiga executar obras,
                pagar os funcionários públicos, pagar as aposentadorias, e cumprir
                com todas suas outras atribuições. Ainda, as receitas públicas podem
                ser divididas em dois grupos que explicam suas origens, as receitas
                primárias e as receitas financeiras. As Receitas Primárias são aquelas
                que já foram citadas aqui anteriormente, advindas dos pagamentos
                dos impostos, taxas e contribuições, sua principal característica é que
                para que o governo arrecade uma receita primária ele não precisa
                contrair nenhuma dívida. Diferente das Receitas Financeiras, que
                nascem de fontes de financiamento, ou seja, é a tomada de
                empréstimos que deverão ser pagos no futuro com juros e correção
                monetária.</p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Orçamento Público, Valor Planejado, Receita Vinculada</h5>
            <p class="font-normal text-gray-700 ">Todos os anos o governo elabora o orçamento público. O Orçamento
                Público é o documento onde o governo mostra o quanto planeja gastar
                em cada área, e o quanto arrecada de impostos, taxas e contribuições.
                Todas as atividades que o governo pretende exercer naquele ano,
                devem estar dentro do orçamento, e os valores que serão gastos com
                estas atividades deverão ser fixadas. Esse valor fixado no orçamento
                do ano é chamado de Valor Planejado. Sendo assim, para que o
                governo possa realizar qualquer atividade é imprescindível que esta
                obra esteja no orçamento público.</p>
                <p class="font-normal text-gray-700 ">
                    O planejamento dentro do orçamento público é muito importante para
                que o governo tenha noção dos valores que ele terá disponível para
                gastar com determinadas atividades, pois nem toda receita disponível
                poderá ser utilizada conforme o governo deseja. Algumas receitas
                específicas entram para o governo já destinadas, elas só poderão ser
                gastas conforme é determinado na legislação brasileira. Estas receitas
                com destinação são chamadas de Receitas Vinculadas.
                </p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Despesas Obrigatórias, Despesas Discricionárias, Orçamento Autorizativo</h5>
            <p class="font-normal text-gray-700 ">Além das receitas vinculadas, alguns gastos públicos são considerados
                como obrigatórios. As Despesas Obrigatórias são aquelas que o
                governo não tem discricionariedade, ou seja, não tem liberdade para
                agir. Um exemplo de despesa obrigatória são os gastos mínimos que
                o governo deve aplicar em educação e saúde previstos na constituição
                federal. Após esta divisão, resta ao orçamento discutir sobre as
                Despesas Discricionárias, aquelas em que o poder público tem o
                poder de agir e decidir.</p>
                <p class="font-normal text-gray-700 ">
                    Assim que o orçamento é aprovado e entra em vigor naquele ano, o
                governo está autorizado a começar a realizar as atividades previstas
                naquele documento. É válido ressaltar, que mesmo que um gasto
                esteja previsto no orçamento, isto não é uma garantia de que ele será
                executado, pois o orçamento brasileiro é um Orçamento Autorizativo,
                ou seja, quando a lei orçamentária é aprovada, ela autoriza a
                realização do gasto, mas não a obriga.
                </p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Despesa Pública, Licitação</h5>
            <p class="font-normal text-gray-700 ">Uma obra demanda diversos gastos diferentes, como a contração de
                empresas de engenharia, mão de obra, entre outros. Estes gastos são
                chamados de Despesa pública. A despesa pública é a aplicação dos
                recursos financeiros arrecadados pelo governo, por meio da contração
                de prestadores de serviço, compra de materiais, entre outros. A
                despesa pública deve ser acompanhada de perto pela população e pelo
                governo, e para isso ela é dividida em três etapas: Empenhado;
                Liquidado e Pago.</p>
                <p class="font-normal text-gray-700 ">
                    O processo de compras do governo é um passo muito importante que
                visa garantir a integridade da compra pública. Este processo de
                compra chama-se Licitação. A licitação é uma ordem administrativo
                que busca levantar os possíveis fornecedores de determinado produto,
                e realizar comparações entre preços, e características de qualidade,
                para escolha do melhor fornecedor.
                </p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Empenhado, Liquidado, Pago</h5>
            <p class="font-normal text-gray-700 ">Quando o governo inicia a contração das empresas e a compra dos
                materiais para a realização da obra, ele emite notas de empenho. A
                despesa é chamada de Empenhada, quando nasce a obrigação de
                pagamento da despesa pelo governo. Este, realiza a reserva daquele
                valor para que seja pago assim que o serviço prestado seja concluído
                e/ou o produto seja entregue para o poder público.
                
                Assim que o serviço é prestado e/ou o material é entregue, o governo
                tem o papel de verificar se a entrega foi realizada conforme a
                contratação, e se ela atende as necessidades daquele pedido. Em caso
                afirmativo, a despesa passa para sua segunda etapa, liquidada. A
                despesa Liquidada é exatamente a etapa de verificação e contatação
                daquilo que foi adquirido pelo governo.
                </p>
                <p class="font-normal text-gray-700 ">
                    Ao final deste processo, se na fase de liquidação tudo estiver
                conforme o contratado, o governo poderá realizar o repasse do valor
                ao fornecedor. Essa etapa é justamente a etapa de Pagamento. O
                pagamento é o repasse do valor acordado pelas partes.
                </p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Despesa Primária, Despesa Financeira</h5>
            <p class="font-normal text-gray-700 ">Assim como as receitas, as despesas também podem ser classificadas
                de acordo com sua origem, em despesas primárias e despesas
                financeiras. As Despesas Primárias são aquelas que nascem no
                decorrer dos gastos da atividade pública, e não configuram o
                pagamento de encargos de dívidas. E as Despesas Financeiras são
                aquelas que representam o pagamento da parcela de juros e correção
                monetária das receitas financeiras.
                </p>
        </div>
        <div href="#" class="block p-6 mb-4 max-w bg-white rounded-lg border border-gray-200 shadow-md hover:bg-gray-100">
            <h5 class="mb-2 text-2xl font-bold tracking-tight text-gray-900">Ativo, Passivo</h5>
            <p class="font-normal text-gray-700 ">Outro conceito importante dentro da contabilidade pública são os ativos.
                Ativo é um item que tem a capacidade de criar benefícios econômicos
                ao poder público. Para ser considerado como ativo, ele deve ser
                controlado no presente pelo poder público e deve ser resultado de um
                evento passado, ou seja, eles são o resultado de operações passadas
                como por exemplo sua compra ou produção. Simplificando, os ativos
                são itens como imóveis, máquinas, dinheiro, entre diversos outros, que
                são do poder público, e ao serem utilizados geram benefícios a ele.
                
                Já os passivos representam as obrigações do governo. Por definição,
                passivo é uma obrigação presente, que tem origem em um evento
                passado e que para deixar de existir, deverá envolver uma saída de
                recursos do poder público. Em suma, os passivos podem ser
                exemplificados como as prestações que se tem a pagar de uma compra a
                prazo, por exemplo, ao adquirir equipamento de maneira parcelada, o
                governo terá a obrigação de efetuar os devidos pagamentos, obrigações
                essas que surgiram deum evento passado (a aquisição do bem) e para
                que esta obrigação deixe de existir, o poder público deverá quitá-la. </p>
